def create_user_status(user_id, navigate_to_user_details):
    updated_data = load_user_data(user_id)  # –ó–∞–≤–∞–Ω—Ç–∞–∂—É—î–º–æ –¥–∞–Ω—ñ –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á–∞
